-- Anna
CREATE TABLE AppUser(
    User_ID INT PRIMARY KEY,
    First_Name VARCHAR(255) NOT NULL,
    Last_Name VARCHAR(255) NOT NULL,
    Email VARCHAR(255) NOT NULL,
    Address_Number VARCHAR(255) NOT NULL,
    Phone_Number VARCHAR(255) NOT NULL
);

CREATE TABLE LibraryStaff (
    Staff_ID INT PRIMARY KEY,
    Salary INT NOT NULL,
    Position_Name VARCHAR(255) NOT NULL,
    Position_Type VARCHAR(255) NOT NULL,
    FOREIGN KEY (Staff_ID) REFERENCES AppUser(User_ID)
);


CREATE TABLE SystemAdministrator (
    Staff_ID INT PRIMARY KEY,
    Managees VARCHAR(255),
    FOREIGN KEY (Staff_ID) REFERENCES AppUser(User_ID)
);



--Library Member
CREATE TABLE LibraryMember (
    LibraryMember_ID INT PRIMARY KEY,
    Items_CheckedOut INT NOT NULL,
    Date_Joined DATE NOT NULL,
    Outstanding_Balance DECIMAL(5,2) DEFAULT 0.00 NOT NULL CHECK(Outstanding_Balance >= 0),
    Sex CHAR(1) NOT NULL CHECK (Sex IN ('M','F','O')),
    DOB DATE NOT NULL,
    Status VARCHAR(20) NOT NULL,
    FOREIGN KEY(LibraryMember_ID) REFERENCES AppUser(User_ID) 
);

