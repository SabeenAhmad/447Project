CREATE TABLE Computer_Activity(
    Computer_ID INT NOT NULL,
    User_ID INT NOT NULL,
    Login_Time TIMESTAMP,
    Logout_Time TIMESTAMP,
    FOREIGN KEY(User_ID) REFERENCES AppUser(User_ID),
    PRIMARY KEY (Computer_ID, Login_Time)
